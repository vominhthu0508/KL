    public void ShowResult (ArrayList<KhoaLuanDTO> topkTuples)
    {
        Vector columns = new Vector();
        columns.add("Tuple");
        columns.add("Product Id");
        columns.add("Score");
        columns.add("Probability");
        columns.add("Top-k probability");
        
        Vector vector = new Vector();
        for(KhoaLuanDTO data : topkTuples)
        {
            Vector row = new Vector();            
            row.add(data.getIndexOfTuple());
            row.add(data.getProductId());
            row.add(data.getScore());
            row.add(data.getPro());
            row.add(data.getTopk());
            vector.add(row);
        }
        DefaultTableModel data1 = new DefaultTableModel(vector, columns);
        jTable1.setModel(data1);
    }
